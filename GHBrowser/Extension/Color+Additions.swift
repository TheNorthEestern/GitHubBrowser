import SwiftUI

extension Color {
	// Function to get the RGB components of the color
	func getRGBComponents() -> (r: CGFloat, g: CGFloat, b: CGFloat, a: CGFloat)? {
		var r: CGFloat = 0
		var g: CGFloat = 0
		var b: CGFloat = 0
		var a: CGFloat = 0
		guard UIColor(self).getRed(&r, green: &g, blue: &b, alpha: &a) else { return nil }
		return (r, g, b, a)
	}

	// Function to calculate the relative luminance of the color
	func relativeLuminance() -> CGFloat {
		guard let components = getRGBComponents() else { return 0 }
		let r = components.r <= 0.03928 ? components.r / 12.92 : pow((components.r + 0.055) / 1.055, 2.4)
		let g = components.g <= 0.03928 ? components.g / 12.92 : pow((components.g + 0.055) / 1.055, 2.4)
		let b = components.b <= 0.03928 ? components.b / 12.92 : pow((components.b + 0.055) / 1.055, 2.4)
		return 0.2126 * r + 0.7152 * g + 0.0722 * b
	}

	// Function to calculate the contrast ratio between two colors
	static func contrastRatio(between color1: Color, and color2: Color) -> CGFloat {
		let luminance1 = color1.relativeLuminance()
		let luminance2 = color2.relativeLuminance()
		let brighter = max(luminance1, luminance2)
		let darker = min(luminance1, luminance2)
		return (brighter + 0.05) / (darker + 0.05)
	}
}

